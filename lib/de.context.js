var de = require('./de.js');

require('./de.request.js');
require('./de.response.js');

//  ---------------------------------------------------------------------------------------------------------------  //

de.Context = function(request, response, config) {
    this.request = new de.Request(request);
    this.response = new de.Response(response);
    this.config = config;

    this.state = {};
    this.now = +new Date();
};

